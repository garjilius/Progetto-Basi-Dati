    private void registroPermanenze() throws SQLException{
        
        Vector colonne = new Vector();
        colonne.add("Nome");
        colonne.add("Cognome");
        colonne.add("Codice fiscale");
        colonne.add("Stanza");
        colonne.add("Data arrivo");
        colonne.add("Data partenza");
        
        Vector dati = GestionePermanenza.permanenzeConcluse();
        
        jPanelRadios.setVisible(false);
        jCheckUsed.setVisible(false);
        jTable1.setModel(new DefaultTableModel(dati, colonne));
        jButtonAggiungi.setVisible(false);
        jButtonStipendio.setVisible(false);
        jLabelInfo.setText("#Permanenze per codice fiscale unico: "+GestionePermanenza.contaPermanenzeUniche());
    }
    
